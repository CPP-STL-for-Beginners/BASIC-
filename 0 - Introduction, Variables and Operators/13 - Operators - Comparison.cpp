#include<iostream>
using namespace std;

int main() {
    int a(5), b(10);
    cout << "Comparison Operators :" << endl;
    cout << "a==b : " << (a==b) << endl;
    cout << "a!=b : " << (a!=b) << endl;
    cout << "a<b : " << (a<b) << endl;
    cout << "a>b : " << (a>b) << endl;
    cout << "a<=b : " << (a<=b) << endl;
    cout << "a>=b : " << (a>=b) << endl;
    return 0;
}